public class StrengthTraining extends Exercise
{
    private int reps;
    private int sets;

    // Reps getter and setter
    public int getReps()
    {
        return this.reps;
    }

    public void setReps(int reps)
    {
        this.reps = reps;
    }


    // Sets getter and setter

    public int getSets()
    {
        return this.sets;
    }

    public void setSets(int sets)
    {
        this.sets = sets;
    }
}
